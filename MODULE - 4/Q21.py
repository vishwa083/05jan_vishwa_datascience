#What are oops concepts? Is multiple inheritance supported in java
"Class: Blueprint for creating objects, defining attributes and behaviors."
"Object: Instance of a class, encapsulating data and behavior."
"Encapsulation: Bundling data and methods within a class for information hiding."
"Inheritance: Mechanism for a class to inherit properties and behaviors from another."
"Polymorphism: Treating objects of different classes as objects of a common base class."
"Abstraction: Simplifying complex systems by modeling essential properties and behaviors."

"Java doesn't support Multiple Inheritance, but we can use interfaces to achieve the same purpose."